# List Tuple String.

for i in range(5): # Start at 0, end before 5
    print(i)

print("-----")

for i in range(2, 11, 2):  # Start at 2, end before 11, step by 2
    print(i)

print("-----")

fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

print("-----")

text = "Hello"

for char in text:
    print(char)

print("-----")

person = {"name": "srinivas", "age": 36, "city": "Hyderabad"}

for key, value in person.items():
    print(f"{key}: {value}")

print("-----")