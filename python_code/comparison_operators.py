'''
Comparison Operators:
== Equal To
!= Not Equal To
>  Greater Than
>= Greater Than and Equal To
< Less Than
<= Less Than and Equal To
'''

# Output: True or False

print(10 == 5)

print(10 != 5)

print(10 > 5)

print(10 >= 5)

print(10 < 5)

print(10 <= 5)

print("Hello World" == "HELLO WORLD")
print("Hello World" != "HELLO WORLD")

