# Print Each Letter in the string.

for i in "Happy New Year 2025":
    print(i)


# Print each value from List.

places = ["Hyderabad","Bangalore","Mumbai","Chennai","Delhi","Kolkatta"]

for place in places:
    print(place)

# Numbers
numbers = [2,4,6,8,10]

for number in numbers:
    print(number)


# Range Function

for number in range(0,5):
    print(number)


for number in range(0,5,1):
    print(number)

for number in range(50,100,10):
    print(number)


for number in range(100,50,-10):
    print(number)
