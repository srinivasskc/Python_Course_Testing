"""
Calculations
"""

def add(a,b):
    """
    This is addition function
    """
    return a+b


def sub(a,b):
    """
    This is subraction Function
    """
    return a-b

def multi(a,b):
    """
    This is multiplication function
    """
    return a * b

def div(a,b):
    """
    This is division function
    """
    return a/b

def mod(a,b):
    """
    This is modulus function
    """
    return a%b

